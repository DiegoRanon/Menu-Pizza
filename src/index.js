function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Pizza({ name, ingredient, photoName, price, soldOut }) {
  if (soldOut) return null;

  return (
    <div className="pizza">
      <img src={photoName} alt="Pizza spinaci" />
      <div>
        <h3>{name}</h3>
        <p>{ingredient}</p>
        <span>Price : {price}</span>
      </div>
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Co.</h1>
    </header>
  );
}

function Menu() {
  return (
    <div className="menu">
      <h2>Our menu</h2>
      {/* Helps to know if there are no pizzas */}
      {pizzaData ? (
        <ul className="pizzas">
          {pizzaData.map((p) => {
            return (
              <Pizza
                name={p.name}
                ingredient={p.ingredients}
                photoName={p.photoName}
                price={p.price}
                soldOut={p.soldOut}
              />
            );
          })}
        </ul>
      ) : (
        <span>There are no pizzas in the menu.</span>
      )}
      {/* <Pizza
        name="Pizza Spinaci"
        ingredient="Tomato, mozarella, spinach, and ricotta cheese"
        photoName="pizzas/spinaci.jpg"
        price="10"
      /> */}
    </div>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;

  return (
    <footer className="footer">
      {isOpen ? (
        <div className="order">
          <p>We're open until {closeHour}.</p>
          <button className="btn">Order</button>
        </div>
      ) : (
        <p>
          We're happy to welcome you between {openHour}:00 to {closeHour}:00.
        </p>
      )}
    </footer>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
